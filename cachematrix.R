## Write a short comment describing this function
## Scoping using similar to the example. Alternatively, function could be 
## extended using return instead to make things perhaps easier to read for 
## people not so familiar with. 
makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


